public class DiamondX {

    public static void main (String [] args)
    {
        top ();
        low();
        top();
        low();

    }
    public static void top ()
    {
        System.out.println("   /\\");
        System.out.println("  /  \\");
        System.out.println(" /    \\");

    }
    public static void low()
    {
        System.out.println(" \\    /");
        System.out.println("  \\  /");
        System.out.println("   \\/");;

    }

}
